MACRO INIT &VAR, &VALUE
    LDA &VALUE
    STA &VAR
MEND

MACRO INCREMENT &VAR
    LDA &VAR
    ADD ONE
    STA &VAR
MEND

MACRO DOUBLE_AND_INCREMENT &VAR
    LDA &VAR
    ADD &VAR
    STA &VAR
    INCREMENT &VAR
MEND

MAIN START 0
    INIT VAR1, FIVE
    INIT VAR2, TEN
    DOUBLE_AND_INCREMENT VAR1
    INCREMENT VAR2
    LDA VAR1
    ADD VAR2
    STA RESULT

RSUB

ONE         WORD 1
FIVE        WORD 5
TEN         WORD 10
VAR1        RESW 1
VAR2        RESW 1
RESULT      RESW 1

END MAIN

